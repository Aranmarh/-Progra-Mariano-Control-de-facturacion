    public ArrayList<factura> readf(int idCliente){
        conexion c  = new conexion();
        ArrayList<factura> lista = new ArrayList<>();

          try {
                 String sql = "SELECT * FROM factura WHERE idCliente="+idCliente+"";
                PreparedStatement ps = c.connectDb().prepareStatement(sql);
                ResultSet rs=ps.executeQuery();
                while(rs.next()){
                factura p= new factura(rs.getInt("idFactura"), rs.getString("fecha"), rs.getInt("idCliente"));
                lista.add(p);
                }
                ps.close();
                ps=null;
                c.desconectar();
          } catch (Exception e) {
              System.out.println("Fallo metodo");
          }
          return lista;
    }
    
    
