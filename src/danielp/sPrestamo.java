    public ArrayList<ClienteC> readC(){
        conexion c  = new conexion();
        ArrayList<ClienteC> lista = new ArrayList<>();

          try {
                 String sql = "SELECT * FROM cliente";
                PreparedStatement ps = c.connectDb().prepareStatement(sql);
                ResultSet rs=ps.executeQuery();
                while(rs.next()){
                ClienteC p =  new  ClienteC(rs.getInt("idCliente"), rs.getString("nombre"), rs.getString("apellido"), rs.getInt("telefono"), rs.getString("direccion"));
                lista.add(p);
                }
                ps.close();
                ps=null;
                c.desconectar();
          } catch (Exception e) {
              System.out.println("Fallo metodo");
          }
          return lista;
  
  
  }
    public factura getFactura(int id){
    
    conexion c  = new conexion();
    try {
           String sql = "SELECT f.idFactura, c.idCliente, f.fecha,sum(p.precioVenta) as Total FROM factura f join cliente c on c.idCliente=f.idCliente join detalle d on d.idfactura=f.idFactura join producto p on d.idproducto=p.idProducto where f.idFactura = "+id+"";
           PreparedStatement ps = c.connectDb().prepareStatement(sql);
           ResultSet rs=ps.executeQuery();
           rs.next();
           factura f =  new  factura(rs.getInt("f.idFactura"), rs.getString("f.fecha"), rs.getInt("c.idCliente"), rs.getFloat("Total"));
           
           ps.close();
           ps=null;
           c.desconectar();
           return f;
          } catch (Exception e) {
              System.out.println("Fallo metodo");
          }
          return null;
    }
    
