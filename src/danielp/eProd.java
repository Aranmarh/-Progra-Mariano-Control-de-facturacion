  public ArrayList<productoC> read(){
    conexion c  = new conexion();
    ArrayList<productoC> lista = new ArrayList<>();

      try {
             String sql = "SELECT * FROM producto";
            PreparedStatement ps = c.connectDb().prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            while(rs.next()){
            productoC p= new productoC(rs.getInt("idProducto"), rs.getString("nombre"), rs.getDouble("precioVenta"), rs.getDouble("precioCompra"), rs.getInt("cantidad"));
            lista.add(p);
            }
            ps.close();
            ps=null;
            c.desconectar();
      } catch (Exception e) {
          System.out.println("Fallo metodo");
      }
      return lista;
  
  
  }
  
  
  public void refresh(){
      while(model.getRowCount()>0){
          model.removeRow(0);
      }
      ArrayList<productoC> lista= read();
      for (productoC p: lista) {
          Object item[]= new Object[5];
          item[0]=p.getProducto();
          item[1]=p.getNombre();
          item[2]=p.getPrecioVenta();
          item[3]=p.getPrecioCompra();
          item[4]=p.getCantidad();
          model.addRow(item);
        
      }
  
        jTable1.setModel(model);
  
  }

