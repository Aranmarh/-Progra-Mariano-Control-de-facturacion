    public boolean delete(int id){
        conexion c = new conexion();
        try {
            String sql= "DELETE FROM producto WHERE idProducto=?";
            PreparedStatement ps = c.connectDb().prepareStatement(sql);
            ps.setInt(1, id);
            ps.execute();
            ps.close();
            ps=null;
            c.desconectar();
            return true;
        } catch (Exception e) {
            return false;
        }
    
    }
    
    public boolean update(productoC p){
        conexion c = new conexion();
        try {
            String sql= "UPDATE producto SET nombre=?, precioVenta=?,precioCompra=?,cantidad=? WHERE idProducto=?";
            PreparedStatement ps = c.connectDb().prepareStatement(sql);
            ps.setString(1, p.getNombre());
            ps.setDouble(2, p.getPrecioVenta());
            ps.setDouble(3, p.getPrecioCompra());
            ps.setDouble(4, p.getCantidad());
            ps.setInt(5, p.getProducto());
            ps.execute();
            ps.close();
            ps=null;
            c.desconectar();
            return true;
        } catch (Exception e) {
            return false;
        }

}
    
